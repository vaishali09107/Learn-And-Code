def is_guess_below_target(guess, target_number):
    """ Checks if the guess is below target """
    return guess < target_number

def is_guess_above_target(guess, target_number):
    """ Checks if the guess is above target """
    return guess > target_number

def is_guess_matching(guess, target_number):
    """ Checks if the guess matches target """
    return guess == target_number
